# # logical operators(Truth Table)
e=True or False
print(e)

a = False or False
print(a)

a = True or True
print(a)

# "OR" ke lea dono me se koi ek chiz "Trur" hoge to "True" return karenge
# Truth table of 'or'
print("True or False is: ", True or False)
print("True or True is: ",True or True)
print("False or True is: ",False or True)
print("False or False is :",False or False)

# "AND" ke lea jab dono chiz true honge to 'TRUE ' to TRUE return karege
# Truth table of 'and'
print("True and False is: ", True and False)  # ALt dabakar rakhna curser ko aage piche karne ko "Multi curser functionality " kahete hai
print("True and True is: ",True and True)
print("False and True is: ",False and True)
print("False and False is: ",False and False)

print(not(True)) # not sirf ek operand per oprate karta hai Means true ko false false ko true kar deta hai
print(not(False))  # Jo true ko false , false ko true bana de usea not operaror kahete hai